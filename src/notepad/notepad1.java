    private void jMenuItem8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem8ActionPerformed
        // TODO add your handling code here:
        JFileChooser j=new JFileChooser();
        FileNameExtensionFilter f=new FileNameExtensionFilter("only .txt files", "txt");
        j.addChoosableFileFilter(f);
        int action=j.showOpenDialog(this);
        if(action!=JFileChooser.APPROVE_OPTION)
            return;
        File fi=j.getSelectedFile();
        try
        {
            BufferedReader b=new BufferedReader(new FileReader(fi));
            jTextArea1.read(b, null);
        }
        catch(Exception e){}
       
    }//GEN-LAST:event_jMenuItem8ActionPerformed

